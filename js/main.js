function editObject(obj) {
    for(let x in obj) {
        if(isFinite(+obj[x])) {
            obj[x] = obj[x] / 2;
            if(obj[x] % 2 !== 0) {
                obj[x] = Math.round(obj[x]);
            }
        } else if(!isFinite(+obj[x])) {
            obj[x] = 'Hello, Palmo';
        } else {
            delete obj[x];
        }
    }
    return obj;
}

